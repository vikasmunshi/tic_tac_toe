char_to_cell = {
    'a': (0, 0), 'b': (0, 1), 'c': (0, 2),
    'd': (1, 0), 'e': (1, 1), 'f': (1, 2),
    'g': (2, 0), 'h': (2, 1), 'i': (2, 2)
}


def memoize(func):
    cache = {}

    def f(*args):
        if args not in cache:
            cache[args] = func(*args)
        return cache[args]

    return f


@memoize
def moves_to_chars(moves):
    return ''.join(cell_to_char[tuple(cell)] for cell in moves)


def strategy(board):
    return char_to_cell[random.choice(board_to_move[moves_to_chars(board.moves)])]
